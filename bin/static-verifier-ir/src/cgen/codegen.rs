//! Streams chunked C source from a [`Program`]: generated constants headers,
//! a constants table, chunk functions, and a driver entry point.

use std::{
    fs::File,
    io::{BufWriter, Write},
    path::{Path, PathBuf},
};

use eyre::Result;
use halo2_base::halo2_proofs::halo2curves::bn256::Fr;
use num_bigint::BigUint;

use super::{biguint_limbs, fr_limbs, fr_modulus};
use crate::{
    chip::baby_bear::BABY_BEAR_MODULUS_U64,
    hash::{Poseidon2Params, POSEIDON2_COMPRESS_PARAMS, POSEIDON2_PARAMS},
    ir::ir::{walk, Opcode, Program},
};

const OPCODES_H: &str = include_str!("../../c/opcodes.h");

pub struct GenOut {
    pub c_files: Vec<PathBuf>,
    pub num_chunks: usize,
}

fn limbs_lit(l: [u64; 4]) -> String {
    format!(
        "{{{{0x{:016x}ULL, 0x{:016x}ULL, 0x{:016x}ULL, 0x{:016x}ULL}}}}",
        l[0], l[1], l[2], l[3]
    )
}

fn fr_lit(f: Fr) -> String {
    limbs_lit(fr_limbs(f))
}

fn plain_limbs_lit(l: [u64; 4]) -> String {
    format!("{{0x{:016x}ULL, 0x{:016x}ULL, 0x{:016x}ULL, 0x{:016x}ULL}}", l[0], l[1], l[2], l[3])
}

/// `-p^{-1} mod 2^64` for CIOS.
fn neg_inv_p0(p0: u64) -> u64 {
    // Newton: x <- x * (2 - p0 * x), doubling correct bits each step.
    let mut inv = 1u64;
    for _ in 0..6 {
        inv = inv.wrapping_mul(2u64.wrapping_sub(p0.wrapping_mul(inv)));
    }
    debug_assert_eq!(p0.wrapping_mul(inv), 1);
    inv.wrapping_neg()
}

fn write_fr_constants(dir: &Path) -> Result<()> {
    use openvm_stark_sdk::{
        openvm_stark_backend::p3_field::{extension::BinomiallyExtendable, PrimeField64},
        p3_baby_bear::BabyBear,
    };

    let p = fr_modulus();
    let r: BigUint = (BigUint::from(1u64) << 256) % &p;
    let r2 = (r.clone() * &r) % &p;
    let half_p = (&p - 1u64) / 2u64;
    let p_minus_2 = &p - 2u64;
    let bb = BABY_BEAR_MODULUS_U64;
    let p_mod_bb = (&p % bb).to_u64_digits()[0] as u32;
    let w = <BabyBear as BinomiallyExtendable<4>>::W.as_canonical_u64();

    let mut f = BufWriter::new(File::create(dir.join("fr_constants.h"))?);
    writeln!(f, "// generated by static-verifier-ir codegen; do not edit")?;
    writeln!(f, "#pragma once")?;
    writeln!(f, "static const fr_t FR_MODULUS = {};", limbs_lit(biguint_limbs(&p)))?;
    writeln!(f, "#define FR_INV 0x{:016x}ULL", neg_inv_p0(biguint_limbs(&p)[0]))?;
    writeln!(f, "static const fr_t FR_R2 = {};", limbs_lit(biguint_limbs(&r2)))?;
    writeln!(f, "static const fr_t FR_ONE_MONT = {};", limbs_lit(biguint_limbs(&r)))?;
    writeln!(
        f,
        "static const uint64_t FR_P_MINUS_2[4] = {};",
        plain_limbs_lit(biguint_limbs(&p_minus_2))
    )?;
    writeln!(
        f,
        "static const uint64_t FR_HALF_P[4] = {};",
        plain_limbs_lit(biguint_limbs(&half_p))
    )?;
    writeln!(f, "#define BB_P 0x{bb:08x}u")?;
    writeln!(f, "#define FR_P_MOD_BB 0x{p_mod_bb:08x}u")?;
    writeln!(f, "#define BB_W {w}u")?;
    writeln!(f, "static const fr_t FR_BB_W_MONT = {};", fr_lit(Fr::from(w)))?;
    Ok(())
}

fn write_poseidon2_table<const T: usize>(
    f: &mut impl Write,
    prefix: &str,
    params: &Poseidon2Params<T>,
) -> Result<()> {
    writeln!(f, "#define {prefix}_ROUNDS_F {}", params.rounds_f)?;
    writeln!(f, "#define {prefix}_ROUNDS_P {}", params.rounds_p)?;
    writeln!(f, "static const fr_t {prefix}_EXTERNAL_RC[{}][{T}] = {{", params.rounds_f)?;
    for rc in &params.external_rc {
        let row: Vec<String> = rc.iter().map(|&c| fr_lit(c)).collect();
        writeln!(f, "    {{{}}},", row.join(", "))?;
    }
    writeln!(f, "}};")?;
    writeln!(f, "static const fr_t {prefix}_INTERNAL_RC[{}] = {{", params.rounds_p)?;
    for &rc in &params.internal_rc {
        writeln!(f, "    {},", fr_lit(rc))?;
    }
    writeln!(f, "}};")?;
    let diag: Vec<String> = params.mat_internal_diag_m_1.iter().map(|&c| fr_lit(c)).collect();
    writeln!(f, "static const fr_t {prefix}_DIAG[{T}] = {{{}}};", diag.join(", "))?;
    Ok(())
}

fn write_poseidon2_constants(dir: &Path) -> Result<()> {
    let mut f = BufWriter::new(File::create(dir.join("poseidon2_constants.h"))?);
    writeln!(f, "// generated by static-verifier-ir codegen; do not edit")?;
    writeln!(f, "#pragma once")?;
    write_poseidon2_table(&mut f, "P2T3", &POSEIDON2_PARAMS)?;
    write_poseidon2_table(&mut f, "P2T2", &POSEIDON2_COMPRESS_PARAMS)?;
    Ok(())
}

fn write_consts_c(dir: &Path, consts: &[Fr]) -> Result<PathBuf> {
    let path = dir.join("consts.c");
    let mut f = BufWriter::new(File::create(&path)?);
    writeln!(f, "#include \"opcodes.h\"")?;
    writeln!(f, "const fr_t IR_CONSTS[{}] = {{", consts.len().max(1))?;
    for &c in consts {
        writeln!(f, "    {},", fr_lit(c))?;
    }
    writeln!(f, "}};")?;
    Ok(path)
}

fn emit_inst(f: &mut impl Write, op: Opcode, aux: u32, a: &[u32], o: u32) -> Result<()> {
    use Opcode::*;
    match op {
        Input => writeln!(f, "w[{o}] = in[{aux}];")?,
        Const => writeln!(f, "w[{o}] = IR_CONSTS[{aux}];")?,
        Add | BbAdd => writeln!(f, "w[{o}] = fr_add(w[{}], w[{}]);", a[0], a[1])?,
        Sub | BbSub => writeln!(f, "w[{o}] = fr_sub(w[{}], w[{}]);", a[0], a[1])?,
        Mul | BbMul => writeln!(f, "w[{o}] = fr_mul(w[{}], w[{}]);", a[0], a[1])?,
        Neg | BbNeg => writeln!(f, "w[{o}] = fr_neg(w[{}]);", a[0])?,
        MulAdd | BbMulAdd => {
            writeln!(f, "w[{o}] = fr_mul_add(w[{}], w[{}], w[{}]);", a[0], a[1], a[2])?
        }
        SubMul => writeln!(f, "w[{o}] = fr_sub_mul(w[{}], w[{}], w[{}]);", a[0], a[1], a[2])?,
        Select => writeln!(f, "w[{o}] = fr_select(w[{}], w[{}], w[{}]);", a[0], a[1], a[2])?,
        IsZero => writeln!(f, "op_is_zero(&w[{o}], w[{}]);", a[0])?,
        DivModU32 => writeln!(f, "op_divmod_u32(&w[{o}], w[{}], {aux}u);", a[0])?,
        Decompose => {
            let num_limbs = aux >> 8;
            let limb_bits = aux & 0xff;
            writeln!(f, "op_decompose(&w[{o}], w[{}], {num_limbs}u, {limb_bits}u);", a[0])?
        }
        BnToBb5 => writeln!(f, "op_bn_to_bb5(&w[{o}], w[{}]);", a[0])?,
        Poseidon2T3 => {
            writeln!(f, "op_poseidon2_t3(&w[{o}], w[{}], w[{}], w[{}]);", a[0], a[1], a[2])?
        }
        Poseidon2T2 => writeln!(f, "op_poseidon2_t2(&w[{o}], w[{}], w[{}]);", a[0], a[1])?,
        BbReduce => writeln!(f, "w[{o}] = op_bb_reduce(w[{}]);", a[0])?,
        BbDiv => writeln!(f, "w[{o}] = op_bb_div(w[{}], w[{}]);", a[0], a[1])?,
        Ext4Add => {
            for i in 0..4 {
                writeln!(
                    f,
                    "w[{}] = fr_add(w[{}], w[{}]);",
                    o + i,
                    a[i as usize],
                    a[i as usize + 4]
                )?;
            }
        }
        Ext4Sub => {
            for i in 0..4 {
                writeln!(
                    f,
                    "w[{}] = fr_sub(w[{}], w[{}]);",
                    o + i,
                    a[i as usize],
                    a[i as usize + 4]
                )?;
            }
        }
        Ext4Neg => {
            for i in 0..4 {
                writeln!(f, "w[{}] = fr_neg(w[{}]);", o + i, a[i as usize])?;
            }
        }
        Ext4ScalarMul => {
            for i in 0..4 {
                writeln!(f, "w[{}] = fr_mul(w[{}], w[{}]);", o + i, a[i as usize], a[4])?;
            }
        }
        Ext4ScalarMulAdd => {
            for i in 0..4 {
                writeln!(
                    f,
                    "w[{}] = fr_mul_add(w[{}], w[{}], w[{}]);",
                    o + i,
                    a[i as usize],
                    a[4],
                    a[i as usize + 5]
                )?;
            }
        }
        Ext4Reduce => {
            for i in 0..4 {
                writeln!(f, "w[{}] = op_bb_reduce(w[{}]);", o + i, a[i as usize])?;
            }
        }
        Ext4Mul | Ext4Div => {
            let name = if op == Ext4Mul { "op_ext4_mul" } else { "op_ext4_div" };
            writeln!(
                f,
                "{name}(&w[{o}], w[{}], w[{}], w[{}], w[{}], w[{}], w[{}], w[{}], w[{}]);",
                a[0], a[1], a[2], a[3], a[4], a[5], a[6], a[7]
            )?
        }
    }
    Ok(())
}

fn env_usize(name: &str, default: usize) -> usize {
    std::env::var(name).ok().and_then(|s| s.parse().ok()).unwrap_or(default)
}

pub fn generate(prog: &Program, dir: &Path) -> Result<GenOut> {
    std::fs::create_dir_all(dir)?;
    std::fs::write(dir.join("opcodes.h"), OPCODES_H)?;
    write_fr_constants(dir)?;
    write_poseidon2_constants(dir)?;

    let ops_path = dir.join("ops.c");
    std::fs::write(&ops_path, "#define SVIR_OPCODES_IMPL\n#include \"opcodes.h\"\n")?;
    let mut c_files = vec![ops_path, write_consts_c(dir, &prog.consts)?];

    let ops_per_fn = env_usize("SVIR_OPS_PER_FN", 4096);
    let fns_per_file = env_usize("SVIR_FNS_PER_FILE", 4);
    let num_chunks = prog.insts.len().div_ceil(ops_per_fn).max(1);

    let mut cur_file: Option<BufWriter<File>> = None;
    let mut err: Option<eyre::Report> = None;
    {
        let mut chunk_idx = 0usize;
        let mut in_chunk = 0usize;
        walk(prog, |idx, op, inst, args, out_base| {
            if err.is_some() {
                return;
            }
            let r = (|| -> Result<()> {
                if idx % ops_per_fn == 0 {
                    chunk_idx = idx / ops_per_fn;
                    if chunk_idx % fns_per_file == 0 {
                        // start new file, closing the open chunk fn in the old one
                        if let Some(mut prev) = cur_file.take() {
                            writeln!(prev, "}}")?;
                            prev.flush()?;
                        }
                        let path = dir.join(format!("chunks_{:04}.c", chunk_idx / fns_per_file));
                        let mut f = BufWriter::new(File::create(&path)?);
                        writeln!(f, "#include \"opcodes.h\"")?;
                        c_files.push(path);
                        cur_file = Some(f);
                    } else if let Some(f) = cur_file.as_mut() {
                        writeln!(f, "}}")?;
                    }
                    let f = cur_file.as_mut().unwrap();
                    writeln!(
                        f,
                        "void svir_chunk_{chunk_idx}(const fr_t* restrict in, fr_t* restrict w) {{"
                    )?;
                    in_chunk = 0;
                }
                let f = cur_file.as_mut().unwrap();
                emit_inst(f, op, inst.aux, args, out_base)?;
                in_chunk += 1;
                Ok(())
            })();
            if let Err(e) = r {
                err = Some(e);
            }
        });
        if let Some(e) = err {
            return Err(e);
        }
        if let Some(mut f) = cur_file.take() {
            writeln!(f, "}}")?;
            f.flush()?;
        }
        let _ = in_chunk;
    }

    // driver
    let driver_path = dir.join("driver.c");
    let mut f = BufWriter::new(File::create(&driver_path)?);
    writeln!(f, "#include \"opcodes.h\"")?;
    for k in 0..num_chunks {
        writeln!(f, "void svir_chunk_{k}(const fr_t* restrict in, fr_t* restrict w);")?;
    }
    writeln!(f, "void svir_tracegen(const fr_t* in, fr_t* w) {{")?;
    for k in 0..num_chunks {
        writeln!(f, "    svir_chunk_{k}(in, w);")?;
    }
    writeln!(f, "}}")?;
    f.flush()?;
    c_files.push(driver_path);

    Ok(GenOut { c_files, num_chunks })
}
